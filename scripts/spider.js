function fetch(id, cookie) {
    return new Promise((resolve, reject) => {
        const options = {
            hostname: 'i.cnblogs.com',
            port: 443,
            path: '/api/posts/' + id,
            method: 'GET',
            headers: {
                'Content-Type': 'application/json',
                'Content-Length': 0,
                cookie: cookie
            },
        };
        const req = https.request(options, res => {
            let data = '';

            res.on('data', chunk => {
                data = data + chunk.toString()
            })
            res.on('end', () => {
                resolve(JSON.parse(data))
            })
        });

        req.on('error', error => {
            reject(error);
        });

        req.write('')
        req.end();
    })
}

